def main():
    # Read two integers from standard input
    a = int(input("enter a number"))
    b = int(input("enter a number"))

    # Calculate the sum, difference, and product
    sum_ab = a + b
    diff_ab = a - b
    prod_ab = a * b

    # Print the results
    print(sum_ab)
    print(diff_ab)
    print(prod_ab)


# Call the main function
if __name__ == "__main__":
    main()
